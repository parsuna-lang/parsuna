/// Per-backend arguments for the Java target.
#[derive(clap::Args, Clone, Debug, Default)]
pub struct Args {
    /// Java package the generated `Grammar` class lives in. Maps directly
    /// to the `package` declaration and to the on-disk directory layout
    /// (`com.example.foo` → `com/example/foo/Grammar.java`). Defaults to
    /// the grammar's name when `None`.
    #[arg(long = "package")]
    pub package: Option<String>,
}

pub fn emit(st: &StateTable, args: &Args) -> Vec<EmittedFile> {
    let pkg = args
        .package
        .clone()
        .filter(|p| !p.is_empty())
        .unwrap_or_else(|| {
            if st.grammar_name.is_empty() {
                "parser".to_string()
            } else {
                st.grammar_name.clone()
            }
        });
    let mut s = String::new();

    writeln!(&mut s, "// Generated by parsuna — do not edit by hand.").unwrap();
    writeln!(&mut s, "//").unwrap();
    writeln!(
        &mut s,
        "// Pull-based, recoverable parser. Obtain a Parser from one of"
    )
    .unwrap();
    writeln!(
        &mut s,
        "// {}.Grammar's static parseXxx factories and iterate it (or call",
        pkg
    )
    .unwrap();
    writeln!(
        &mut s,
        "// nextEvent directly) to walk the parse as a flat Event stream."
    )
    .unwrap();
    writeln!(&mut s, "// Requires dev.parsuna:parsuna-rt.").unwrap();
    writeln!(&mut s, "package {};", pkg).unwrap();
    writeln!(&mut s).unwrap();
    writeln!(&mut s, "import java.io.InputStream;").unwrap();
    writeln!(&mut s, "import java.nio.charset.StandardCharsets;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.DfaMatcher;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.Event;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.Lexer;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.Parser;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.ParserConfig;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.ParserOptions;").unwrap();
    writeln!(&mut s, "import dev.parsuna.runtime.Cursor;").unwrap();
    writeln!(&mut s).unwrap();

    writeln!(
        &mut s,
        "/** Grammar-specific state and entry points. Thin wrapper around dev.parsuna.runtime.Parser. */"
    )
    .unwrap();
    writeln!(&mut s, "public final class Grammar {{").unwrap();
    writeln!(&mut s, "    private Grammar() {{}}").unwrap();
    writeln!(&mut s).unwrap();

    emit_constants(&mut s, st);
    emit_dfa(&mut s, st);
    emit_tables(&mut s, st);
    emit_drive(&mut s, st);
    emit_public_api(&mut s, st);

    writeln!(&mut s, "}}").unwrap();

    // A dotted package (`com.example.foo`) lays out as nested directories.
    let mut pkg_path: PathBuf = pkg.split('.').collect();
    pkg_path.push("Grammar.java");
    vec![EmittedFile {
        path: pkg_path,
        contents: s,
    }]
}

fn emit_constants(s: &mut String, st: &StateTable) {
    writeln!(s, "    /**").unwrap();
    writeln!(
        s,
        "     * Token kinds this grammar can emit. EOF is the runtime end-of-input"
    )
    .unwrap();
    writeln!(
        s,
        "     * sentinel; the rest come from the grammar's `token` declarations."
    )
    .unwrap();
    writeln!(
        s,
        "     * Lex failures (no DFA pattern matched) come through as Token instances"
    )
    .unwrap();
    writeln!(
        s,
        "     * with kind == {{@link dev.parsuna.runtime.Lexer#ERROR_KIND}} (0xFFFF)."
    )
    .unwrap();
    writeln!(s, "     */").unwrap();
    writeln!(s, "    public static enum TokenKind {{").unwrap();
    write!(s, "        EOF(0)").unwrap();
    for t in &st.tokens {
        write!(s, ",\n        {}({})", screaming_snake(&t.name), t.kind).unwrap();
    }
    writeln!(s, ";").unwrap();
    writeln!(s, "        public final int id;").unwrap();
    writeln!(s, "        TokenKind(int id) {{ this.id = id; }}").unwrap();
    writeln!(s, "        public String displayName() {{").unwrap();
    writeln!(s, "            switch (this) {{").unwrap();
    writeln!(s, "                case EOF: return \"EOF\";").unwrap();
    for t in &st.tokens {
        writeln!(
            s,
            "                case {}: return \"{}\";",
            screaming_snake(&t.name),
            t.name
        )
        .unwrap();
    }
    writeln!(s, "                default: return name();").unwrap();
    writeln!(s, "            }}").unwrap();
    writeln!(s, "        }}").unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();

    writeln!(s, "    /**").unwrap();
    writeln!(
        s,
        "     * One variant per non-fragment grammar rule. Attached to Event.Enter/Exit"
    )
    .unwrap();
    writeln!(
        s,
        "     * (via their `rule()` component) to identify each subtree in the stream."
    )
    .unwrap();
    writeln!(s, "     */").unwrap();
    writeln!(s, "    public static enum RuleKind {{").unwrap();
    if st.rule_kinds.is_empty() {
        writeln!(s, "        ;").unwrap();
    } else {
        for (i, n) in st.rule_kinds.iter().enumerate() {
            if i > 0 {
                writeln!(s, ",").unwrap();
            }
            write!(s, "        {}({})", screaming_snake(n), i).unwrap();
        }
        writeln!(s, ";").unwrap();
    }
    writeln!(s, "        public final int id;").unwrap();
    writeln!(s, "        RuleKind(int id) {{ this.id = id; }}").unwrap();
    writeln!(s, "        public String displayName() {{").unwrap();
    writeln!(s, "            switch (this) {{").unwrap();
    for n in &st.rule_kinds {
        writeln!(
            s,
            "                case {}: return \"{}\";",
            screaming_snake(n),
            n
        )
        .unwrap();
    }
    writeln!(s, "                default: return name();").unwrap();
    writeln!(s, "            }}").unwrap();
    writeln!(s, "        }}").unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();
}

fn token_id(st: &StateTable, kind: u16) -> String {
    if kind == 0 {
        return "TokenKind.EOF.id".to_string();
    }
    match st.tokens.iter().find(|t| t.kind == kind) {
        Some(t) => format!("TokenKind.{}.id", screaming_snake(&t.name)),
        None => panic!("unknown token id {} while emitting Java backend", kind),
    }
}

fn rule_id(st: &StateTable, kind: u16) -> String {
    let name = st
        .rule_kinds
        .get(kind as usize)
        .unwrap_or_else(|| panic!("unknown rule kind id {} while emitting Java backend", kind));
    format!("RuleKind.{}.id", screaming_snake(name))
}

fn emit_dfa(s: &mut String, st: &StateTable) {
    writeln!(
        s,
        "    /** Compiled lexer DFA: one switch arm per DFA state, dispatched by mode id. */"
    )
    .unwrap();
    writeln!(
        s,
        "    private static void longestMatch(byte[] buf, int start, int bufLen, int mode, int[] out) {{"
    )
    .unwrap();
    if st.modes.len() == 1 {
        writeln!(s, "        longestMatchMode0(buf, start, bufLen, out);").unwrap();
    } else {
        writeln!(s, "        switch (mode) {{").unwrap();
        for m in &st.modes {
            writeln!(
                s,
                "            case {}: longestMatchMode{}(buf, start, bufLen, out); return;",
                m.id, m.id
            )
            .unwrap();
        }
        writeln!(
            s,
            "            default: out[0] = 0; out[1] = Lexer.ERROR_KIND; out[2] = 0; return;"
        )
        .unwrap();
        writeln!(s, "        }}").unwrap();
    }
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();

    for m in &st.modes {
        writeln!(
            s,
            "    /** Longest-match helper for lexer mode `{}` (id {}). */",
            m.name, m.id
        )
        .unwrap();
        writeln!(
            s,
            "    private static void longestMatchMode{}(byte[] buf, int start, int bufLen, int[] out) {{",
            m.id
        )
        .unwrap();
        writeln!(s, "        int pos = start;").unwrap();
        writeln!(s, "        int bestLen = 0;").unwrap();
        writeln!(s, "        int bestKind = Lexer.ERROR_KIND;").unwrap();
        writeln!(s, "        int state = {};", START).unwrap();
        writeln!(s, "        outer: while (true) {{").unwrap();
        writeln!(s, "            switch (state) {{").unwrap();
        for ds in &m.dfa {
            emit_dfa_state_arm(s, st, ds, m.id);
        }
        writeln!(s, "                default: break outer;").unwrap();
        writeln!(s, "            }}").unwrap();
        writeln!(s, "        }}").unwrap();
        writeln!(s, "        out[0] = bestLen;").unwrap();
        writeln!(s, "        out[1] = bestKind;").unwrap();
        writeln!(s, "        out[2] = pos - start;").unwrap();
        writeln!(s, "    }}").unwrap();
        writeln!(s).unwrap();
    }

    for m in &st.modes {
        for ds in &m.dfa {
            let sl = ds.self_loop_ranges();
            if use_class_lookup_table(sl) {
                writeln!(
                    s,
                    "    /** 256-entry membership table for the self-loop class of"
                )
                .unwrap();
                writeln!(
                    s,
                    "     *  state {} in lexer mode {} (id {}). One static byte load",
                    ds.id, m.name, m.id
                )
                .unwrap();
                writeln!(
                    s,
                    "     *  per scanned byte beats the 3+ chained range checks the"
                )
                .unwrap();
                writeln!(s, "     *  scalar variant compiles to. */").unwrap();
                writeln!(
                    s,
                    "    private static final boolean[] CLASS_M{}_S{} = {};",
                    m.id, ds.id,
                    class_table_initializer(sl)
                )
                .unwrap();
                writeln!(s).unwrap();
            }
        }
    }
    if needs_swar_long_le(st) {
        writeln!(
            s,
            "    /** Little-endian 8-byte view over the lex buffer. Used by"
        )
        .unwrap();
        writeln!(
            s,
            "     *  the SWAR scan-skip prologue in {{@code longestMatchModeN}}"
        )
        .unwrap();
        writeln!(
            s,
            "     *  to test 8 bytes at once for the \"ASCII bytes except a single"
        )
        .unwrap();
        writeln!(
            s,
            "     *  byte X\" classes that dominate text-content / quoted-string"
        )
        .unwrap();
        writeln!(s, "     *  scans. */").unwrap();
        writeln!(
            s,
            "    private static final java.lang.invoke.VarHandle LONG_LE ="
        )
        .unwrap();
        writeln!(
            s,
            "        java.lang.invoke.MethodHandles.byteArrayViewVarHandle(long[].class, java.nio.ByteOrder.LITTLE_ENDIAN);"
        )
        .unwrap();
        writeln!(s).unwrap();
    }
    writeln!(
        s,
        "    private static final DfaMatcher MATCHER = Grammar::longestMatch;"
    )
    .unwrap();
    writeln!(s).unwrap();
    write!(s, "    private static boolean isSkip(int k) {{ return ").unwrap();
    let skip_list: Vec<String> = st
        .tokens
        .iter()
        .filter(|t| t.skip)
        .map(|t| format!("k == {}", token_id(st, t.kind)))
        .collect();
    if skip_list.is_empty() {
        writeln!(s, "false; }}").unwrap();
    } else {
        writeln!(s, "{}; }}", skip_list.join(" || ")).unwrap();
    }
    writeln!(s).unwrap();
}

/// If `ranges` is "ASCII bytes except a single byte X" (i.e. two
/// contiguous ranges that together cover 0x00..=0x7f minus exactly one
/// byte), return X. Self-loops with this shape are the dominant cost
/// for text-content / quoted-string scans, and we can vectorise them
/// with an 8-byte SWAR test.
fn detect_ascii_except_x(ranges: &[(u8, u8)]) -> Option<u8> {
    if ranges.len() != 2 { return None; }
    let (lo1, hi1) = ranges[0];
    let (lo2, hi2) = ranges[1];
    if lo1 != 0x00 || hi2 != 0x7f { return None; }
    if (hi1 as u16) + 2 != lo2 as u16 { return None; }
    Some(hi1 + 1)
}

/// Does any DFA state across any mode have a self-loop the SWAR scan
/// applies to? Emits `LONG_LE` only when at least one such state exists,
/// so single-mode token-only grammars don't carry an unused VarHandle.
fn needs_swar_long_le(st: &StateTable) -> bool {
    for m in &st.modes {
        for ds in &m.dfa {
            if detect_ascii_except_x(ds.self_loop_ranges()).is_some() {
                return true;
            }
        }
    }
    false
}

/// True when the self-loop is a complex byte class that benefits from
/// a 256-entry boolean lookup table. The threshold is "≥3 disjoint
/// ranges, and not the SWAR pattern" — 1- and 2-range checks are
/// cheap enough that JIT folds them into a couple of cmps, but at 3+
/// the table-lookup variant (1 cache-resident byte load + 1 cmp)
/// reliably wins on JIT-compiled scalar dispatch.
fn use_class_lookup_table(ranges: &[(u8, u8)]) -> bool {
    ranges.len() >= 3 && detect_ascii_except_x(ranges).is_none()
}

/// Render a byte class as a Java {@code boolean[256]} initializer
/// expression, marking every byte in any of `ranges` as `true`.
fn class_table_initializer(ranges: &[(u8, u8)]) -> String {
    let mut bits = [false; 256];
    for &(lo, hi) in ranges {
        for b in lo..=hi {
            bits[b as usize] = true;
        }
    }
    let mut s = String::from("new boolean[]{");
    for (i, &v) in bits.iter().enumerate() {
        if i > 0 { s.push(','); }
        s.push_str(if v { "true" } else { "false" });
    }
    s.push('}');
    s
}

fn emit_dfa_state_arm(
    s: &mut String,
    st: &StateTable,
        ds: &DfaState,
    mode_id: u32,
) {
    if ds.arms.is_empty() {
        writeln!(s, "                case {}: break outer;", ds.id).unwrap();
        return;
    }
    writeln!(s, "                case {}: {{", ds.id).unwrap();
    let self_loop = ds.self_loop_ranges();
    if !self_loop.is_empty() {
        if use_class_lookup_table(self_loop) {
            // Lookup-table self-loop scan. The 256-entry boolean[] is
            // static-initialized once and lives at the top of the
            // class — JIT keeps it L1-resident, so the inner test is
            // a single byte load + a single compare per byte. Beats
            // a 5-range chained check on JIT-compiled scalar paths.
            writeln!(
                s,
                "                    while (pos < bufLen && CLASS_M{}_S{}[buf[pos] & 0xFF]) pos++;",
                mode_id, ds.id
            )
            .unwrap();
            if let Some(kind) = ds.accept {
                writeln!(s, "                    bestLen = pos - start;").unwrap();
                writeln!(
                    s,
                    "                    bestKind = {};",
                    token_id(st, kind)
                )
                .unwrap();
            }
            writeln!(s, "                    if (pos >= bufLen) break outer;").unwrap();
            writeln!(s, "                    int b = buf[pos] & 0xFF;").unwrap();
            // Continue to dispatch arms below. We've already done the
            // self-loop work, so we'd otherwise duplicate the inner
            // loop emit; jump directly to the dispatch portion.
            let mut first = true;
            for arm in &ds.arms {
                let cond = byte_cond(&arm.ranges);
                let kw = if first { "if" } else { "else if" };
                first = false;
                write!(
                    s,
                    "                    {} ({}) {{ pos++; state = {};",
                    kw, cond, arm.target
                )
                .unwrap();
                if let Some(kind) = arm.target_accept {
                    write!(
                        s,
                        " bestLen = pos - start; bestKind = {};",
                        token_id(st, kind)
                    )
                    .unwrap();
                }
                writeln!(s, " }}").unwrap();
            }
            writeln!(s, "                    else break outer;").unwrap();
            writeln!(s, "                    break;").unwrap();
            writeln!(s, "                }}").unwrap();
            return;
        }
        if let Some(x) = detect_ascii_except_x(self_loop) {
            // SWAR-8 prologue. Read 8 bytes as a little-endian long via
            // VarHandle (a single load on x86 LE), test all 8 bytes in
            // parallel for `b == X` (the excluded byte) or `b >= 0x80`
            // (high bit / start of UTF-8 multi-byte). When the chunk is
            // all class members, advance pos by 8 and continue. When a
            // terminator hits, jump pos to the first terminator byte and
            // fall through to the per-byte tail.
            let x_word = u64::from(x) * 0x0101010101010101u64;
            writeln!(
                s,
                "                    while (pos + 8 <= bufLen) {{"
            )
            .unwrap();
            writeln!(
                s,
                "                        long w = (long) LONG_LE.get(buf, pos);"
            )
            .unwrap();
            writeln!(
                s,
                "                        long high = w & 0x8080808080808080L;"
            )
            .unwrap();
            writeln!(
                s,
                "                        long xor = w ^ 0x{:016x}L;",
                x_word
            )
            .unwrap();
            writeln!(
                s,
                "                        long isX = (xor - 0x0101010101010101L) & ~xor & 0x8080808080808080L;"
            )
            .unwrap();
            writeln!(
                s,
                "                        long stop = high | isX;"
            )
            .unwrap();
            writeln!(s, "                        if (stop != 0) {{").unwrap();
            writeln!(
                s,
                "                            pos += Long.numberOfTrailingZeros(stop) >>> 3;"
            )
            .unwrap();
            writeln!(s, "                            break;").unwrap();
            writeln!(s, "                        }}").unwrap();
            writeln!(s, "                        pos += 8;").unwrap();
            writeln!(s, "                    }}").unwrap();
        }
        // Per-byte tail. For SWAR'd states this only handles the < 8
        // byte tail (or the terminator byte itself); for non-SWAR
        // states it's the full scan loop and HotSpot's loop optimiser
        // is expected to unroll it.
        writeln!(s, "                    while (pos < bufLen) {{").unwrap();
        writeln!(s, "                        int b = buf[pos] & 0xFF;").unwrap();
        writeln!(
            s,
            "                        if (!({})) break;",
            byte_cond(self_loop)
        )
        .unwrap();
        writeln!(s, "                        pos++;").unwrap();
        writeln!(s, "                    }}").unwrap();
        if let Some(kind) = ds.accept {
            writeln!(s, "                    bestLen = pos - start;").unwrap();
            writeln!(
                s,
                "                    bestKind = {};",
                token_id(st, kind)
            )
            .unwrap();
        }
    }
    writeln!(s, "                    if (pos >= bufLen) break outer;").unwrap();
    writeln!(s, "                    int b = buf[pos] & 0xFF;").unwrap();
    let mut first = true;
    for arm in &ds.arms {
        let cond = byte_cond(&arm.ranges);
        let kw = if first { "if" } else { "else if" };
        first = false;
        write!(
            s,
            "                    {} ({}) {{ pos++; state = {};",
            kw, cond, arm.target
        )
        .unwrap();
        if let Some(kind) = arm.target_accept {
            write!(
                s,
                " bestLen = pos - start; bestKind = {};",
                token_id(st, kind)
            )
            .unwrap();
        }
        writeln!(s, " }}").unwrap();
    }
    writeln!(s, "                    else break outer;").unwrap();
    writeln!(s, "                    break;").unwrap();
    writeln!(s, "                }}").unwrap();
}


fn byte_cond(ranges: &[(u8, u8)]) -> String {
    ranges
        .iter()
        .map(|(lo, hi)| {
            if lo == hi {
                format!("b == 0x{:02x}", lo)
            } else {
                format!("(b >= 0x{:02x} && b <= 0x{:02x})", lo, hi)
            }
        })
        .collect::<Vec<_>>()
        .join(" || ")
}

fn emit_tables(s: &mut String, st: &StateTable) {
    writeln!(s, "    private static final int K = {};", st.k).unwrap();
    for (name, id) in &st.entry_states {
        writeln!(
            s,
            "    private static final int ENTRY_{} = {};",
            name.to_uppercase(),
            id
        )
        .unwrap();
    }

    for f in &st.first_sets {
        if !f.has_references {
            continue;
        }
        let seqs: Vec<String> = f
            .seqs
            .iter()
            .map(|seq| {
                let kinds: Vec<String> = seq.iter().map(|t| token_id(st, *t)).collect();
                format!("{{{}}}", kinds.join(", "))
            })
            .collect();
        writeln!(
            s,
            "    private static final int[][] FIRST_{} = new int[][]{{{}}};",
            f.id,
            seqs.join(", ")
        )
        .unwrap();
    }
    for f in &st.sync_sets {
        writeln!(
            s,
            "    private static final int[] SYNC_{} = new int[]{{{}}};",
            f.id,
            f.kinds
                .iter()
                .map(|t| token_id(st, *t))
                .collect::<Vec<_>>()
                .join(", ")
        )
        .unwrap();
    }
    writeln!(s).unwrap();
}

fn emit_drive(s: &mut String, st: &StateTable) {
    writeln!(s, "    private static Event step(Cursor p) {{").unwrap();
    writeln!(s, "        int cur = p.state();").unwrap();
    writeln!(s, "        Event event = null;").unwrap();
    writeln!(s, "        switch (cur) {{").unwrap();
    for state in st.states.values() {
        writeln!(
            s,
            "            case {}: {{ // {}",
            state.id, state.label
        )
        .unwrap();
        emit_body(s, st, &state.body, "                ");
        writeln!(s, "                break;").unwrap();
        writeln!(s, "            }}").unwrap();
    }
    writeln!(
        s,
        "            default: throw new IllegalStateException(\"unknown state \" + cur);"
    )
    .unwrap();
    writeln!(s, "        }}").unwrap();
    writeln!(s, "        p.setState(cur);").unwrap();
    writeln!(s, "        return event;").unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();
}

fn emit_instr(s: &mut String, st: &StateTable, op: &Instr, ind: &str) {
    match op {
        Instr::Enter(k) => {
            writeln!(s, "{}event = p.enter({});", ind, rule_id(st, *k)).unwrap();
        }
        Instr::Exit(k) => {
            writeln!(s, "{}event = p.exit({});", ind, rule_id(st, *k)).unwrap();
        }
        Instr::Expect { kind, token_name, sync, .. } => {
            writeln!(
                s,
                "{}event = p.tryConsume({}, SYNC_{}, \"{}\");",
                ind,
                token_id(st, *kind),
                sync,
                token_name
            )
            .unwrap();
        }
        Instr::PushRet(r) => {
            writeln!(s, "{}p.pushRet({});", ind, r).unwrap();
        }
    }
}

fn emit_tail(s: &mut String, st: &StateTable, tail: &Tail, ind: &str) {
    match tail {
        Tail::Jump(n) => {
            writeln!(s, "{}cur = {};", ind, n).unwrap();
        }
        Tail::Ret => {
            writeln!(s, "{}cur = p.popRet();", ind).unwrap();
        }
        Tail::Star { first, body, cont, head } => {
            let inner = format!("{}    ", ind);
            emit_first_check(s, st, *first, ind);
            writeln!(s, "{}p.pushRet({});", inner, head).unwrap();
            emit_body(s, st, body, &inner);
            writeln!(s, "{}}}", ind).unwrap();
            match cont {
                Some(n) => writeln!(s, "{}else cur = {};", ind, n).unwrap(),
                None => writeln!(s, "{}else cur = p.popRet();", ind).unwrap(),
            }
        }
        Tail::Opt { first, body, cont } => {
            let inner = format!("{}    ", ind);
            emit_first_check(s, st, *first, ind);
            if let Some(n) = cont {
                writeln!(s, "{}p.pushRet({});", inner, n).unwrap();
            }
            emit_body(s, st, body, &inner);
            writeln!(s, "{}}}", ind).unwrap();
            match cont {
                Some(n) => writeln!(s, "{}else cur = {};", ind, n).unwrap(),
                None => writeln!(s, "{}else cur = p.popRet();", ind).unwrap(),
            }
        }
        Tail::Dispatch { tree, sync, cont } => {
            emit_dispatch_tree(s, st, tree, *sync, *cont, ind);
        }
    }
}

/// Emit the opening `if (...) {` of a Star/Opt match check. At K=1 this
/// is an inline `p.look(0).kind == X || ...` test; at K>1 it falls back
/// to the runtime's matchesFirst over the materialized FIRST array.
fn emit_first_check(s: &mut String, st: &StateTable, first: u32, ind: &str) {
    if st.k == 1 {
        let kinds = first_set_singleton_kinds(st, first);
        let expr = kinds
            .iter()
            .map(|k| format!("p.look(0) == {}", k))
            .collect::<Vec<_>>()
            .join(" || ");
        writeln!(s, "{}if ({}) {{", ind, expr).unwrap();
    } else {
        writeln!(s, "{}if (p.matchesFirst(FIRST_{})) {{", ind, first).unwrap();
    }
}

/// Flatten a K=1 first set's sequences into a sorted, deduped list of
/// token-kind ids. Asserts the sequences are LL(1) singletons.
fn first_set_singleton_kinds(st: &StateTable, first: u32) -> Vec<u16> {
    let mut kinds: Vec<u16> = st.first_sets[first as usize]
        .seqs
        .iter()
        .map(|seq| {
            assert_eq!(
                seq.len(),
                1,
                "first_set_singleton_kinds expects LL(1) singletons"
            );
            seq[0]
        })
        .collect();
    kinds.sort_unstable();
    kinds.dedup();
    kinds
}

fn emit_body(s: &mut String, st: &StateTable, body: &Body, ind: &str) {
    for op in &body.instrs {
        emit_instr(s, st, op, ind);
    }
    emit_tail(s, st, &body.tail, ind);
}

fn emit_dispatch_tree(
    s: &mut String,
    st: &StateTable,
    tree: &DispatchTree,
    sync: u32,
    cont: Option<u32>,
    ind: &str,
) {
    match tree {
        DispatchTree::Leaf(leaf) => {
            writeln!(s, "{}{{", ind).unwrap();
            emit_dispatch_leaf_block(s, st, leaf, sync, cont, &format!("{}  ", ind));
            writeln!(s, "{}}}", ind).unwrap();
        }
        DispatchTree::Switch {
            depth,
            arms,
            default,
        } => {
            writeln!(s, "{}switch (p.look({})) {{", ind, depth).unwrap();
            let inner = format!("{}  ", ind);
            for (kind, sub) in arms {
                let literal = format!("{}", *kind);
                writeln!(s, "{}case {}: {{", inner, literal).unwrap();
                emit_dispatch_tree(s, st, sub, sync, cont, &format!("{}  ", inner));
                writeln!(s, "{}  break;", inner).unwrap();
                writeln!(s, "{}}}", inner).unwrap();
            }
            writeln!(s, "{}default: {{", inner).unwrap();
            emit_dispatch_leaf_block(s, st, default, sync, cont, &format!("{}  ", inner));
            writeln!(s, "{}  break;", inner).unwrap();
            writeln!(s, "{}}}", inner).unwrap();
            writeln!(s, "{}}}", ind).unwrap();
        }
    }
}

fn emit_dispatch_leaf_block(
    s: &mut String,
    st: &StateTable,
    leaf: &DispatchLeaf,
    sync: u32,
    cont: Option<u32>,
    ind: &str,
) {
    match (leaf, cont) {
        (DispatchLeaf::Arm(b), Some(n)) => {
            writeln!(s, "{}p.pushRet({});", ind, n).unwrap();
            emit_body(s, st, b, ind);
        }
        (DispatchLeaf::Arm(b), None) => emit_body(s, st, b, ind),
        (DispatchLeaf::Fallthrough, Some(n)) => writeln!(s, "{}cur = {};", ind, n).unwrap(),
        (DispatchLeaf::Fallthrough, None) => writeln!(s, "{}cur = p.popRet();", ind).unwrap(),
        (DispatchLeaf::Error, Some(n)) => {
            writeln!(s, "{}cur = {};", ind, n).unwrap();
            writeln!(s, "{}event = p.errorHere(\"unexpected token\");", ind).unwrap();
            writeln!(s, "{}p.recoverTo(SYNC_{});", ind, sync).unwrap();
        }
        (DispatchLeaf::Error, None) => {
            writeln!(s, "{}event = p.errorHere(\"unexpected token\");", ind).unwrap();
            writeln!(s, "{}p.recoverTo(SYNC_{});", ind, sync).unwrap();
            writeln!(s, "{}cur = p.popRet();", ind).unwrap();
        }
    }
}

fn emit_apply_actions(s: &mut String, st: &StateTable) {
    use crate::lowering::ModeActionInfo;

    let action_tokens: Vec<&crate::lowering::TokenInfo> = st
        .tokens
        .iter()
        .filter(|t| !t.mode_actions.is_empty())
        .collect();

    writeln!(
        s,
        "    /** Apply per-token mode-stack actions to {{@code lex}}. Generated as"
    )
    .unwrap();
    writeln!(
        s,
        "     *  a switch over token kinds; tokens without {{@code -> push/pop}}"
    )
    .unwrap();
    writeln!(s, "     *  fall through with no effect. */").unwrap();
    writeln!(
        s,
        "    private static void applyActions(int kind, Lexer lex) {{"
    )
    .unwrap();
    if action_tokens.is_empty() {
        writeln!(s, "        // grammar declares no mode actions").unwrap();
    } else {
        writeln!(s, "        switch (kind) {{").unwrap();
        for t in &action_tokens {
            // Java `case` labels must be constant expressions, so emit
            // the numeric token-kind id directly rather than the named
            // TokenKind.X.id accessor.
            writeln!(s, "            case {}: {{", t.kind).unwrap();
            for action in &t.mode_actions {
                match action {
                    ModeActionInfo::Push(id) => {
                        writeln!(s, "                lex.pushMode({});", id).unwrap();
                    }
                    ModeActionInfo::Pop => {
                        writeln!(s, "                lex.popMode();").unwrap();
                    }
                }
            }
            writeln!(s, "                break;").unwrap();
            writeln!(s, "            }}").unwrap();
        }
        writeln!(s, "            default: break;").unwrap();
        writeln!(s, "        }}").unwrap();
    }
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();
}

fn emit_public_api(s: &mut String, st: &StateTable) {
    emit_apply_actions(s, st);
    writeln!(
        s,
        "    private static final ParserConfig CONFIG = new ParserConfig(K, Grammar::isSkip, Grammar::step, Grammar::applyActions);"
    )
    .unwrap();
    writeln!(s).unwrap();
    writeln!(
        s,
        "    private static Parser fromInputStream(InputStream in, int entry, ParserOptions opts) {{"
    )
    .unwrap();
    writeln!(
        s,
        "        return new Parser(new Lexer(in, MATCHER), entry, CONFIG, opts);"
    )
    .unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();
    writeln!(
        s,
        "    private static Parser fromBytes(byte[] data, int entry, ParserOptions opts) {{"
    )
    .unwrap();
    writeln!(
        s,
        "        return new Parser(new Lexer(data, MATCHER), entry, CONFIG, opts);"
    )
    .unwrap();
    writeln!(s, "    }}").unwrap();
    writeln!(s).unwrap();
    for (name, _) in &st.entry_states {
        let pname = pascal(name);
        let upper = name.to_uppercase();
        writeln!(s).unwrap();
        writeln!(
            s,
            "    /** Parse the `{name}` rule from an InputStream (read lazily in 16 KiB chunks). Skip tokens are surfaced as Event.Token; call parse{pname}NoSkips or pass a ParserOptions with dropSkips=true to silently consume them. */",
        )
        .unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}(InputStream in) {{ return fromInputStream(in, ENTRY_{upper}, new ParserOptions()); }}"
        ).unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}(InputStream in, ParserOptions opts) {{ return fromInputStream(in, ENTRY_{upper}, opts); }}"
        ).unwrap();
        writeln!(
            s,
            "    /** Parse the `{name}` rule from an in-memory byte buffer (zero-copy — the Lexer holds the array by reference). */",
        )
        .unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}(byte[] data) {{ return fromBytes(data, ENTRY_{upper}, new ParserOptions()); }}"
        ).unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}(byte[] data, ParserOptions opts) {{ return fromBytes(data, ENTRY_{upper}, opts); }}"
        ).unwrap();
        writeln!(
            s,
            "    /** Parse the `{name}` rule from a String (UTF-8 encoded internally). */",
        )
        .unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}(String src) {{ return parse{pname}(src.getBytes(StandardCharsets.UTF_8)); }}"
        ).unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}(String src, ParserOptions opts) {{ return parse{pname}(src.getBytes(StandardCharsets.UTF_8), opts); }}"
        ).unwrap();
        writeln!(
            s,
            "    /** Parse the `{name}` rule and silently consume skip tokens. */",
        )
        .unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}NoSkips(InputStream in) {{ return fromInputStream(in, ENTRY_{upper}, new ParserOptions(true)); }}"
        ).unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}NoSkips(byte[] data) {{ return fromBytes(data, ENTRY_{upper}, new ParserOptions(true)); }}"
        ).unwrap();
        writeln!(
            s,
            "    public static Parser parse{pname}NoSkips(String src) {{ return parse{pname}NoSkips(src.getBytes(StandardCharsets.UTF_8)); }}"
        ).unwrap();
    }
}

